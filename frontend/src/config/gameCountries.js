// Helper functions for the game
export const getAllGameCountries = () => Object.keys(GAME_COUNTRIES);

export const getCountryByRegion = (region) => {
  return Object.entries(GAME_COUNTRIES)
    .filter(([_, country]) => country.region === region)
    .reduce((acc, [code, country]) => {
      acc[code] = country;
      return acc;
    }, {});
};

export const getRegions = () => {
  const regions = new Set();
  Object.values(GAME_COUNTRIES).forEach(country => regions.add(country.region));
  return Array.from(regions).sort();
};

export const isValidGameCountry = (countryCode) => {
  return countryCode in GAME_COUNTRIES;
};

export const getCountryInfo = (countryCode) => {
  return GAME_COUNTRIES[countryCode] || null;
};